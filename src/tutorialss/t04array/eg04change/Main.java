package tutorialss.t04array.eg04change;

public class Main {
  public static void main(String[] args) {
    int[] arr = {10, 20, 30};

    arr[0] = 100;
    arr[1] = 200;
    arr[2] = 300;

    System.out.println(arr[0]);
    System.out.println(arr[1]);
    System.out.println(arr[2]);
  }
}

